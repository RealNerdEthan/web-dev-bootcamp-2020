//Example of functions we've made so far, called Function Statement
function add(x, y) {
    return x + y;
}

//This is a Function Expression. It shows that Functions are treated liek Arrays and other things, simply a value and can be passed into arguments and etc.
const add = function (x, y) {
    return x + y;
}

